//toString - transforma em string

let n = 5

let res = n.toString() + 5

console.log(res)

//toFixed - coloca duas casas decimais

let nFixed = 54.987654321

let resFixed = 'R$ ' + nFixed.toFixed(2)

console.log(resFixed)

//parseInt - transforma em number

let nParseInt = '8'

let resParseInt = parseInt(nParseInt) + 2

console.log(resParseInt)

//parseFloat - transforma em number mantendo as casas decimais

let nParseFloat = '30.15'

let resParseFloat = parseFloat(nParseFloat) + 2

console.log(resParseFloat)
