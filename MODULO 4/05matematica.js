//Arredondar

let valorRound = Math.round(3.57)
console.log(valorRound)

let valorFloor = Math.floor(3.57) //pra baixo
console.log(valorFloor)

let valorCeil = Math.ceil(3.57) //pra cima
console.log(valorCeil)

//Arredondar trocando de sinal
let negativo = Math.abs(-4)
console.log(negativo)

//Retornar o menor numero de uma lista
let lista = Math.min(7, 3, 80, 4, 52)
console.log(lista)

//Retornar o maior numero de uma lista
let max = Math.max(7, 3, 80, 4, 52)
console.log(max)

//Retornar um numero aleatorio entre 0 e 1
let random = Math.random()
console.log(random)

//Retornar um numero aleatorio entre 0 e 100
let randomNovo = Math.floor(Math.random() * 100)
console.log(randomNovo)
